"""
Python Variables
This file demonstrates basic usage of variables in Python.
"""

# Integer variable
age = 21
print("Age:", age)

# Float variable
height = 5.9
print("Height:", height)

# String variable
name = "Harshit"
print("Name:", name)

# Boolean variable
is_student = True
print("Is Student:", is_student)

# Dynamic typing example
x = 10
print("x value:", x)

x = "Python"
print("x value after reassignment:", x)
