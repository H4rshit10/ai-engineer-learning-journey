"""
Boolean and Numeric Data Types in Python
"""

# Boolean data type
is_active = True
is_admin = False

print(is_active, type(is_active))
print(is_admin, type(is_admin))

# Numeric data types

# Integer
count = 10
print(count, type(count))

# Float
price = 99.99
print(price, type(price))

# Complex
value = 2 + 3j
print(value, type(value))
