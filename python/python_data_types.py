"""
Python Data Types Examples
"""

# Integer
age = 21
print("Age:", age, type(age))

# Float
price = 99.99
print("Price:", price, type(price))

# String
language = "Python"
print("Language:", language, type(language))

# Boolean
is_learning = True
print("Is Learning:", is_learning, type(is_learning))

# List
numbers = [1, 2, 3, 4]
print("Numbers:", numbers, type(numbers))

# Tuple
coordinates = (10, 20)
print("Coordinates:", coordinates, type(coordinates))

# Dictionary
student = {"name": "Harshit", "age": 21}
print("Student:", student, type(student))

# Set
unique_numbers = {1, 2, 3}
print("Unique Numbers:", unique_numbers, type(unique_numbers))
